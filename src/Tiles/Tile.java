package Tiles;

public class Tile {
    public int x,y;
    public int startX,startY;
    public int width,height;

    public Tile(){
    }

    public Tile(int x, int y, int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }

}
